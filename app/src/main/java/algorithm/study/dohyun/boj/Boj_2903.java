package algorithm.study.dohyun.boj;
import java.util.*;
public class Boj_2903 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int tmp = 2;
		int num = 1;
		
		
		for(int i=0; i<n; i++) {
				
			
			num = num*2;
		}
			
		tmp = tmp + (num-1);
			
		System.out.println(tmp*tmp);
		
		
		

}}
